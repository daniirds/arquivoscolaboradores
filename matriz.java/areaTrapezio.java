public class areaTrapezio {
    public static void main(String[] args) {
        double baseMaior = 8;
        double baseMenor = 5;
        double altura = 4;

        double areaTrapezio = ((baseMaior + baseMenor) * altura) / 2;
        System.out.println(" A área do trapézio é: " + areaTrapezio);
    }
}